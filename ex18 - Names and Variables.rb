def print_Two(arg1,arg2)
	puts "value 1: #{arg1} & value 2: #{arg2}"
end

def print_Two_Once_More(arg1,arg2)
	puts "Print two value 1: #{arg1} & value 2: #{arg2}"
end

def print_one(arg1)
	puts "Print one value 1: #{arg1}"
end

def print_no()
	puts "Print no one"
end

print_Two("Viswanathan", "Anand")
print_Two_Once_More("ViswanathanTwo", "AnandTwo")
print_one("One!")
print_no()